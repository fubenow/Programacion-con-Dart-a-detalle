// Demos el punto de partida a nuestro programa.
void main() {
  //Escribamos un programa que imprima tu nombre en la consola de comandos.
  String nombre = "John";
  print(nombre);

  // Ahora escribamoslo utilizando comillas simples.
  String nombreComillasSimples = 'John';
  print(nombreComillasSimples);

  // Ahora escribamos un String multilínea.
  String nombreMultilinea = '''John Marcos
Videla Rivera
  ''';
  print(nombreMultilinea);

  // Ahora escribamos un String pero con saltos de línea.
  String saltoDeLinea = "John Marcos \nVidela Rivera";
  print(saltoDeLinea);
}
